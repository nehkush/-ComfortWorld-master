package com.app.model;

import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.Table;
import lombok.Data;

@Data
@Entity
@Table(name = "microwaves")
public class Microwave {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column
    private String brand;

    @Column
    private String model;

    @Column   
    private double capacity; // In liters

    @Column
    private boolean hasConvection;

    @Column
    private double price;

    // Additional fields for microwave repair service
    @Column
    private String isUnderWarranty;

    @Column
    private String isServiceRequired;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getBrand() {
		return brand;
	}

	public void setBrand(String brand) {
		this.brand = brand;
	}

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public double getCapacity() {
		return capacity;
	}

	public void setCapacity(double capacity) {
		this.capacity = capacity;
	}

	public boolean isHasConvection() {
		return hasConvection;
	}

	public void setHasConvection(boolean hasConvection) {
		this.hasConvection = hasConvection;
	}

	public double getPrice() {
		return price;
	}

	public void setPrice(double price) {
		this.price = price;
	}

	public String getIsUnderWarranty() {
		return isUnderWarranty;
	}

	public void setIsUnderWarranty(String isUnderWarranty) {
		this.isUnderWarranty = isUnderWarranty;
	}

	public String getIsServiceRequired() {
		return isServiceRequired;
	}

	public void setIsServiceRequired(String isServiceRequired) {
		this.isServiceRequired = isServiceRequired;
	}

    // Getters, setters, equals, hashCode, and toString methods generated by Lombok
}
